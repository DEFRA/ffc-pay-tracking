const {
  BATCH_REJECTED,
  BATCH_QUARANTINED,
  PAYMENT_REJECTED,
  PAYMENT_DAX_REJECTED,
  PAYMENT_INVALID_BANK,
  PAYMENT_PROCESSING_FAILED,
  PAYMENT_SETTLEMENT_UNMATCHED,
  RESPONSE_REJECTED,
  PAYMENT_REQUEST_BLOCKED,
  PAYMENT_DAX_UNAVAILABLE,
  RECEIVER_CONNECTION_FAILED
} = require('../constants/warnings')

const {
  BATCH_REJECTED_STATUS,
  BATCH_QUARANTINED_STATUS,
  PAYMENT_REJECTED_STATUS,
  PAYMENT_DAX_REJECTED_STATUS,
  PAYMENT_INVALID_BANK_STATUS,
  PAYMENT_PROCESSING_FAILED_STATUS,
  PAYMENT_SETTLEMENT_UNMATCHED_STATUS,
  RESPONSE_REJECTED_STATUS,
  PAYMENT_REQUEST_BLOCKED_STATUS,
  PAYMENT_DAX_UNAVAILABLE_STATUS,
  RECEIVER_CONNECTION_FAILED_STATUS
} = require('../constants/statuses')

const getWarningStatus = (event) => {
  const eventMap = {}
  eventMap[BATCH_REJECTED] = BATCH_REJECTED_STATUS
  eventMap[BATCH_QUARANTINED] = BATCH_QUARANTINED_STATUS
  eventMap[PAYMENT_REJECTED] = PAYMENT_REJECTED_STATUS
  eventMap[PAYMENT_DAX_REJECTED] = PAYMENT_DAX_REJECTED_STATUS
  eventMap[PAYMENT_INVALID_BANK] = PAYMENT_INVALID_BANK_STATUS
  eventMap[PAYMENT_PROCESSING_FAILED] = PAYMENT_PROCESSING_FAILED_STATUS
  eventMap[PAYMENT_SETTLEMENT_UNMATCHED] = PAYMENT_SETTLEMENT_UNMATCHED_STATUS
  eventMap[RESPONSE_REJECTED] = RESPONSE_REJECTED_STATUS
  eventMap[PAYMENT_REQUEST_BLOCKED] = PAYMENT_REQUEST_BLOCKED_STATUS
  eventMap[PAYMENT_DAX_UNAVAILABLE] = PAYMENT_DAX_UNAVAILABLE_STATUS
  eventMap[RECEIVER_CONNECTION_FAILED] = RECEIVER_CONNECTION_FAILED_STATUS
  return eventMap[event.type]
}

module.exports = {
  getWarningStatus
}
